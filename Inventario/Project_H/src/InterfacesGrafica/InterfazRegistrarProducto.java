    public void cargarcombox() {

        CategoriaDAO cf = new CategoriaDAO();

        List ya = cf.ListarCargo();
        Iterator<Categoria> cargarList = ya.iterator();
        while (cargarList.hasNext()) {
            Categoria iterando = cargarList.next();
            combogg.addItem(iterando.getCategoria());
        }
    }

