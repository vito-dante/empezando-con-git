    private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed
        // TODO add your handling code here:
        int cbio = 0;
        producto = ptxt.getText();
        cantidad = Integer.parseInt(ctxt.getText());
        categoria = (String) combogg.getSelectedItem();
        fechaingreso = fecha.getDate();
        observacion = obs.getText();

        CategoriaDAO buscar = new CategoriaDAO();

        Iterator<Categoria> inserc = buscar.BCambiar(categoria).iterator();
        while (inserc.hasNext()) {
            Categoria partido = inserc.next();
            cbio = partido.getIdCategoria();
        }
        ProductoDAO operacion1 = new ProductoDAO();

        Estado est = new Estado();
        Inventario inv=new Inventario();
        inv.setIdInventario(1);
        est.setIdEstado(1);
        Categoria rg = new Categoria();
        rg.setIdCategoria(cbio);
        Producto pers = new Producto();
        ///ingresando valores al objeto
        pers.setInventario(inv);
        pers.setEstado(est);
        pers.setCodProd(producto);
        pers.setCantidad(cantidad);
        pers.setCategoria(rg);
        pers.setFechaIngreso(fechaingreso);
        pers.setObservacion(observacion);
        /// insertando a la base de datos por medio 
        operacion1.insertarProducto(pers);

        LimpiarDatos();
        mostrardatos();


    }//GEN-LAST:event_RegistrarActionPerformed

