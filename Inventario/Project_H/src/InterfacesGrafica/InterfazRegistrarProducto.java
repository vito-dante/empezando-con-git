    public void mostrardatos() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Producto");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Categoria");
        modelo.addColumn("Fecha Ingreso");
        modelo.addColumn("Observacion");
        String datosCargar[];
        ProductoDAO activar = new ProductoDAO();
        List aqui = activar.ListarProducto();

        Iterator<Producto> itrPartidos = aqui.iterator();
        while (itrPartidos.hasNext()) {

            Producto partido = itrPartidos.next();

            Categoria hacercambio = new Categoria();
            hacercambio = partido.getCategoria();
            //aqui tiene que visualizar la cantidad de la tabla inventario
            datosCargar = new String[]{partido.getCodProd(), partido.getCantidad().toString(), hacercambio.getCategoria(), partido.getFechaIngreso().toString(), partido.getObservacion()};
            modelo.addRow(datosCargar);
        }
        tabla.setModel(modelo);
       // tabla.setDefaultRenderer(Object.class, new MyRender());
    }

