    private void EntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EntrarActionPerformed
        Criptografia cr=new Criptografia();
        cr.MD5(txt_usuario.getText());
       
        String in_us, in_cl;
        
        in_us = cr.MD5(txt_usuario.getText());
        in_cl = cr.MD5(txt_clave.getText());
        login = new Login_userDAO(in_us, in_cl);
        String xxx = "" + login.tienedatos();

        switch (xxx.charAt(0)) {
            case '1':
                errores.setVisible(true);
                txt_clave.setBackground(Color.WHITE);
                txt_usuario.setBackground(Color.WHITE);
                try {
                    iniciar_autentificacion();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "El sistema no se pudo conectar \ncon la base de datos ");
                }
                break;
            case '0':
                errores.setText("Los 2 Campos No Pueden ser Nulos");
                errores.setVisible(true);
                txt_usuario.setBackground(new java.awt.Color(255, 102, 102));
                txt_clave.setBackground(new java.awt.Color(255, 102, 102));
                break;
            case '2':
                errores.setText("Ingrese un nombre de usuario");
                errores.setVisible(true);
                txt_usuario.setBackground(new java.awt.Color(255, 102, 102));
                txt_clave.setBackground(Color.WHITE);
                break;
            case '3':
                errores.setText("Ingrese su contrase√±a");
                errores.setVisible(true);
                txt_usuario.setBackground(Color.WHITE);
                txt_clave.setBackground(new java.awt.Color(255, 102, 102));
                break;
        }
    }//GEN-LAST:event_EntrarActionPerformed
