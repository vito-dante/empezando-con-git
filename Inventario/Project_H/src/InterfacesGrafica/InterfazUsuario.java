    public void iniciar_autentificacion() {
        InterfazPrincipal x;
        login.esUsuario();
        if (login.getLog() != true) {
            errores.setText("");
            errores.setVisible(true);
            JOptionPane.showMessageDialog(null, "Error : \n Sus Datos No Son Correctos");
            // limpiar();
        } else {
            //login.getIdUsuario(),login.getIdPersona()
            x = new InterfazPrincipal();
            x.setVisible(login.getLog());
            this.dispose();
//---------------------------------------------------------------------------------------            
            JOptionPane.showMessageDialog(x, "Bienvenido");
        }
    }

